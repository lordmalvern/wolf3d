#include "libft.h"

int	ft_isint(char *s)
{
	int l;

	l = ft_strlen(s);
	if (*s == '+' || *s == '-')
		s++;
	if (!*s)
		return (0);
	if (l >= 10 && ft_strcmp(s, "2147483648") >= 0)
		return (0);
	while (*s)
		if (!ft_isdigit(*s++))
			return (0);
	return (1);
}
